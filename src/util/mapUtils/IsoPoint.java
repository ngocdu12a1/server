public class IsoPoint {
    public double x;
    public double y;

    public IsoPoint(int x, int y) {
        this.x = (double) x;
        this.y = (double) y;
    }

    public IsoPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
